package conditions;

public class Switch_condition {

	public static void main(String[] args) {
		
	int days=2;
	switch(days)
	{
	case 1:System.out.println("welcome to sunday ");
	break;
	case 2:System.out.println("welcome to monday");
	
	
	case 3:System.out.println("welcome to tuensday ");
	break;
	
	case 4:System.out.println("welcome to wensday ");
	break;
	
	case 7:System.out.println("welcome to friday");
	break;
	case 8:System.out.println("welcome to  saturday");
	break;
	default :System.out.println("incorrect days ");
	break;
	
	
	}

	}

}
