package loops;

public class For_loops {

	public static void main(String[] args) 
	{
		//1---20
		//start point--1---initilazation
		//condition<=20
		//increament/decreament

		for(int i=1;i<=20;i++)
		{
			System.out.println(i);
		}
		
		for(int j=10;j>=1;j--)
		{
		System.out.println(j);	
		}	
	}
	
	

}
