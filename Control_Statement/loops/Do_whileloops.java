package loops;

public class Do_whileloops {

	public static void main(String[] args) {
		//1---20
		int i=1;
		do {
			System.out.println("value of i is"+i);
			
			i++;
		}
		while(i<=20);
		int j=20;
		do 
		{
			System.out.println("value of j is"+j);
			j--;
		}
        while(j>=1);
	}

}
